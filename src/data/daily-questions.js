export function getDailyQuestion() {
  const now = new Date()
  const startOfYear = new Date(now.getFullYear(), 0, 0)
  const dayOfYear = Math.floor((now - startOfYear) / (1000 * 60 * 60 * 24))
  return DAILY_QUESTIONS[dayOfYear % DAILY_QUESTIONS.length]
}

