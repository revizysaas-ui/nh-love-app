-- QUIZ SESSIONS
CREATE TABLE IF NOT EXISTS quiz_sessions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  room_id BIGINT REFERENCES rooms(id) ON DELETE CASCADE,
  creator TEXT NOT NULL,
  questions JSONB NOT NULL,
  creator_answers JSONB,
  partner_answers JSONB,
  score INTEGER DEFAULT 0,
  completed BOOLEAN DEFAULT false,
  created_at TIMESTAMPTZ DEFAULT now()
);
ALTER TABLE quiz_sessions ENABLE ROW LEVEL SECURITY;
DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='quiz_sessions' AND policyname='Public quiz_sessions') THEN
    CREATE POLICY "Public quiz_sessions" ON quiz_sessions FOR ALL USING (true) WITH CHECK (true);
  END IF;
END $$;

