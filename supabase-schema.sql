-- Supprimer les anciennes tables
DROP TABLE IF EXISTS drawings CASCADE;
DROP TABLE IF EXISTS photos CASCADE;
DROP TABLE IF EXISTS messages CASCADE;
DROP TABLE IF EXISTS questions CASCADE;
DROP TABLE IF EXISTS rooms CASCADE;

-- Table ROOMS
CREATE TABLE rooms (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code TEXT UNIQUE NOT NULL,
  name1 TEXT DEFAULT 'Toi',
  name2 TEXT DEFAULT 'Lui/Elle',
  start_date DATE DEFAULT '2024-01-15',
  next_meeting DATE DEFAULT '2026-08-15',
  city1_name TEXT DEFAULT 'Paris',
  city1_lat DOUBLE PRECISION DEFAULT 48.8566,
  city1_lng DOUBLE PRECISION DEFAULT 2.3522,
  city2_name TEXT DEFAULT 'Los Angeles',
  city2_lat DOUBLE PRECISION DEFAULT 34.0522,
  city2_lng DOUBLE PRECISION DEFAULT -118.2437,
  created_at TIMESTAMPTZ DEFAULT now()
);
ALTER TABLE rooms ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public rooms" ON rooms FOR ALL USING (true) WITH CHECK (true);

-- MESSAGES
CREATE TABLE messages (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  room_id BIGINT REFERENCES rooms(id) ON DELETE CASCADE,
  author TEXT NOT NULL DEFAULT 'Anonyme',
  text TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT now()
);
ALTER TABLE messages ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public messages" ON messages FOR ALL USING (true) WITH CHECK (true);

-- MESSAGES : ajout colonne image
ALTER TABLE messages ADD COLUMN IF NOT EXISTS image_url TEXT;

-- PHOTOS
CREATE TABLE IF NOT EXISTS photos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  room_id BIGINT REFERENCES rooms(id) ON DELETE CASCADE,
  caption TEXT DEFAULT '',
  storage_path TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT now()
);
ALTER TABLE photos ENABLE ROW LEVEL SECURITY;
DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='photos' AND policyname='Public photos') THEN
    CREATE POLICY "Public photos" ON photos FOR ALL USING (true) WITH CHECK (true);
  END IF;
END $$;

-- NOTIFICATIONS
CREATE TABLE IF NOT EXISTS notifications (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
